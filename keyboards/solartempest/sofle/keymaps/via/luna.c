	static void print_logo_narrow(void) {
		oled_set_cursor(0,3);
		oled_write("SOLAR", false);
		oled_set_cursor(0,4);
		oled_write("TMPST", false);
	 
	 	/* wpm counter */
		oled_set_cursor(0,10);
		oled_write_P(PSTR("WPM: "), false);
		oled_write(get_u8_str(get_current_wpm(), ' '), false);
	}
	 
